from typing import Any
from xml.etree import ElementTree

_DOCX_CORE_NAMESPACES = {
    "cp": (
        "http://schemas.openxmlformats.org/package/2006/"
        "metadata/core-properties"
    ),
    "dc": "http://purl.org/dc/elements/1.1/",
    "dcterms": "http://purl.org/dc/terms/",
}
_DOCX_SYNTHETIC_CREATORS = {"python-docx"}
_DATE_PATTERNS = [
    re.compile(r"Q[1-4]\s*20\d{2}", re.IGNORECASE),
    re.compile(r"20\d{2}-\d{2}-\d{2}"),
    re.compile(
        r"(?:January|February|March|April|May|June|July|August|"
        r"September|October|November|December)\s+20\d{2}",
        re.IGNORECASE,
    ),
    re.compile(r"FY\s*20\d{2}", re.IGNORECASE),
]
_VALUE_TOKEN = (
    r"[\$\£\€\¥C]?\$?\s*\d[\d,]*(?:\.\d+)?"
    r"(?:\s*(?:%|bp|bps|pt|pts|d|days|k|m|mm|b|bn)"
    r"(?![A-Za-z]))?"
)
_METRIC_RE = re.compile(
    r"([A-Z][A-Za-z0-9_ /&\-]{1,60}?)"
    r"\s*[:=]\s*"
    r"(" + _VALUE_TOKEN + r")"
    r"\s*(?:[).,;:]*)$",
    re.IGNORECASE,
)
_METRIC_AT_RE = re.compile(
    r"([A-Za-z][A-Za-z0-9_ /&\-\(\)]{1,60}?)"
    r"\s+at\s+"
    r"(" + _VALUE_TOKEN + r")"
    r"\s*(?:[).,;:]*)$",
    re.IGNORECASE,
)
